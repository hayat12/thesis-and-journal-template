<p id="Cover-Page"><h3>Cover Page </h3></p>
the cover page should include the research's titile, author's name, the faculty and finally the date. Write a catchy title and be as concise as possible. Think of something that will grab the reader's attention and keep them intersted. However, it is advisable to have discusstion wiht the supervisor and htere might be a change in the title during the writing process.
<p id="Content-Page"><h3>Content Page </h3></p>
Write the content of a proposal and provide bird's-eye view about your thesis by following appropriate style.
<p id="Introduction-Research-Background"><h3> Introduction: Research Background</h3></p>
provide a necessary introduction on a topic, subject or object in a research. State the purpose of the study, demonstrate your knowledge of the topic and its importance, describeany major issues, key research points and explain any boundaries of the study. Keep it brief, but powerful.
<p id="Problem-Statement"><h3>Problem Statement </h3></p>
List all problem and issue in question format. the questions should be between 3 to 5 questions telling the reader what you intended to study. By using a numbering, the 5'W's can be used to spark the discussion about the problem. Remember, your problem statement is the backbone of the proposal and the feasibility report.
<p id="Objictives-t"><h3>Objectives </h3></p>
The objectives should be clear, rational, relevant and achievable. Answer the questions that has been stated in the problem statement and it should contain exploratory verbs, such as e.g : to describe, to identify, to analyze, to explan, to compare, to differentiate, to distinguish etc.
<p id="Significance-of-Study"><h3>Significance of Study </h3></p>
Discuss the importance of the proposed research, its relevance and the potential impact of your results. The investigation might be relevant for theory, practice and future research. You should explain why it is important for the study to be undertaken and indicate the likelihood of its contribution to the advancement of knowledge. Readers will want to know the benefits and possible drawbcks of your research.
<p id="Limitation-of-Study"><h3>Limitation of Study </h3></p>
The scope of the research is very important to ensure that assessors are able to understand the boundary of study. Do clarify the restrictions or limitations of the place depend on types of research.
<p id="Definition-of-Terms"><h3>Definition of Terms </h3></p>
It is a list of words from your research's title that may need a further explanation to understand. The meaning of words can be referred to dictionaries or any other academic's references. This section explains the key concepts contained in the proposed title. This explanation will help ealuators to better understand the proposal title and scope of study.
<p id="Literature-Review"><h3>Literature Review </h3></p>
A literature review is a section that either first type, discusses published information including theses, dissertations, jounal articles, conference proceedings, books, internet, reports, newspapers and magazines. Or second type, it is conclusion from all resources that you get. It can be just a simple summary of the sources, methods, advantages and deficiencies of 5 to 10 previous studies in the sae niche researhes.  
<p id="Methodology"><h3>Methodology </h3></p>
Explain the implementation of your methods. Demonstrate your knowedge on the methods you used and explain how they benefit your research. State clearly how you plan to conduct your research by describing the research instrument, determining sample degign, data collecting and anlysis of data. The activities should be described with as much detail as possible, and the continuity between them should be apparent.
<p id="Research-Organization"><h3>Research Organization </h3></p>
List the section of your research (headings and indented sub-headings) and the corresponding page numbers.
<p id="References"><h3>References</h3></p>
Provide a resource review. This section outlines all sources you have used in your research and shows your knowledge on the topic. Formatting should follow the standard method advised by faculty of Quranic and Sunnah Studies, USIM. Please refer to USIM's Handbook On Academic Writing.
<p id="Work-Schedule"><h3>Work Schedule </h3></p>
Plan your stages of the research writing in time line or tabular format e.g Gantt chart followed by the deadlines for completion of these stages or tasks.


